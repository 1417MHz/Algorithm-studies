# 개 출력하기

# |\_/|
# |q p|   /}
# ( 0 )"""\
# |"^"`    |
# ||_/=\\__|

print("|\_/|")
print("|q p|   /}")
print('( 0 )"""\\')  # \'앞에 \을 붙여준다.
print('|"^"`    |')
print("||_/=\\\__|")  # \\ 앞에 \을 하나 더 붙여준다.

# 백 슬레시(＼)와 따옴표를 붙여 사용하면＼뒤 따옴표가
# 문자열을 구성하는 기호로 인식되지 못한다. 
# 이것을 방지하기 위해서 앞에 백 슬래시를 한번 더 붙여줘야 한다.
